unsigned long dna_fast_encode(unsigned char* barcode){

	int i, len;
	unsigned long next_digit, result;

	if (barcode == NULL){
		return -1;
	}
	result = 0;
	next_digit = 0;
	len = strlen((char *)barcode);
	for (i = 0; i < len; i+=4){
		next_digit = __dna_4x_encode(barcode+i);
		result = result << 8;
		result = result | next_digit;
	}

	return result;	
}


